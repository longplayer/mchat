    const sizeClass = computed(() => props.size)
    const svgElement = ref(null)
    const config = {
      selector: '#mcat-animated', // some plugins accept DOM elements (svgElement.value), not this one :(
      duration: '2000',
      // can pass in a function or a string like 'easeOutQuint'
      easing: function (t) {
        return t * t
      },
    }
    const atAnimationEnds = () => {
      const childList = Array.prototype.slice.call(svgElement.value.children)
      childList.forEach((e) => {
        e.classList.add('is-end')
      })
    }
    onMounted(() => {
      animation = new Walkway(config)
      animation.draw(atAnimationEnds)
    })
    return { svgElement, sizeClass }
  },
}
</script>

